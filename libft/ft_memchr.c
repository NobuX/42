void	*ft_memchr(const void *s, int c, size_t n)
{
	const char	*s1;

	s1 = s;
	while (n > 0)
	{
		if (s1[n - 1] == c)
			return ((void *)s + n - 1);
		n--;
	}
	return (NULL);
}
